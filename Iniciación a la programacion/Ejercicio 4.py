#Escribe un programa que calcule el área de una finca rectangular en metros cuadrados,
# así como su perímetro exterior, también en metros.


base = float(input('Escribe los metros de la base de la finca:' ))
altura = float(input('Escribe la altura de la finca:' ))
print ('Los metros cuadrados de la finca son:',(base*altura),('en metros cuadrados'))
print ('El perímetro de la finca es',(base*2) + (altura*2), ('en metros cuadrados'))
